rules = {}
rules.default = nil

input.split("\n").each do |l|
  row = l.split(" => ")
  rules[row[0]] = row[1]
end

def rotate(grid)
  lines = grid.split('/')
  ptr = 0
  result = []
  while ptr < lines[0].length
    newl = ""
    lines.each do |l|
      newl += l[ptr]
    end
    ptr += 1
    result << newl
  end
  return result.join('/')
end

def flip(grid)
  newGrid = []
  grid.split('/').each do |g|
    newGrid << g.reverse
  end
  return newGrid.join('/')
end

def permutate(grid)
  leftGrid = rotate(grid)
  rotations = [grid, leftGrid, grid.reverse, leftGrid.reverse]

  perms = []
  rotations.each do |r|
    perms << r
    perms << flip(r)
  end
  return perms
end

def splitGrid(size, start)
  grids = []
  start.split('/').each_slice(size) do |rows|
    ptr = 0
    while ptr < rows[0].length
      newGrid = []
      rows.each do |r|
        newGrid << r[ptr, size]
      end
      grids << newGrid.join("/")
      ptr += size
    end
  end
  return grids
end

def buildStringFromGrid(grids)
  gridSize = Math.sqrt(grids.length).to_i

  answer = []
  grids.each_slice(gridSize) do |tiles|
    (0..(tiles[0].split('/').length - 1)).each do |x|
      bigRow = ""
      tiles.each do |t|
        bigRow += t.split('/')[x]
      end
      answer << bigRow
    end
  end

  return answer.join('/')
end

